	private boolean andLogica(int pos, int num1, int num2){
		boolean res = true;
		String op;
		int suma;
		char valor;
		
		if(pos < 1 || pos > 9){
			res = false;
		}
		else{
			suma = num1 + num2;
			op = Integer.toString(suma);
			
			valor = op.charAt(pos);
			
			if(valor == '0'){  // 0 and 0 -> 0
				res = false;
			}
			else if(valor == '1'){  // 0 and 1 -> 0
				res = false;
			}
			else if(valor == '2'){  // 1 and 1 -> 1
				res = true;
			}
			
		}
		
		return res;
	}
	
